    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String course = (String)jComboBox1.getSelectedItem();
        String branchName = (String)jComboBox2.getSelectedItem();
        String roll = jTextField1.getText();
        String name = jTextField2.getText();
        String gender = (String)jComboBox3.getSelectedItem();
        String fathername = jTextField3.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "123456"); //fix address tyo reach to database.
            Statement st = con.createStatement();
            st.executeUpdate("insert into student(rollno, course, branchName, name, gender, fathername) value('"+roll+"', '"+course+"', '"+branchName+"', '"+name+"', '"+gender+"', '"+fathername+"')");
            JOptionPane.showMessageDialog(null, "Data save successfully");
            setVisible(false);
            new adminHome().setVisible(true);
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    
