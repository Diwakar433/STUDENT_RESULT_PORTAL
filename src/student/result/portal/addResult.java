    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
       
        String roll = jTextField1.getText();
        String physics = jTextField2.getText();
        String em = jTextField3.getText();
        String dbms = jTextField4.getText();
        String os = jTextField5.getText();
        String cn = jTextField6.getText();

        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "123456"); //fix address tyo reach to database.
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno = '"+roll+"'");
            
            if(rs.next()) {
                st.executeUpdate("insert into result(rollno, physics, em, dbms, os, cn) value('"+roll+"', '"+physics+"', '"+em+"', '"+dbms+"', '"+os+"', '"+cn+"')");
                JOptionPane.showMessageDialog(null, "Result added successfully.");
                setVisible(false);
                new addResult().setVisible(true);
            }
            else {
                JOptionPane.showMessageDialog(null, "This RollNo is not registered");
            }
           
           
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

