    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollno = jTextField4.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "123456"); //fix address tyo reach to database.
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student inner join result where student.rollno = '"+rollno+"' and result.rollno = '"+rollno+"'");
            
            if(rs.next()) {
                jTextField12.setText(rs.getString(2));
                jTextField10.setText(rs.getString(3));
                jTextField3.setText(rs.getString(4));
                jTextField4.setText(rs.getString(1));
                jTextField5.setText(rs.getString(6));
                jTextField11.setText(rs.getString(5));
                
               //subject textfield set
                jTextField2.setText(rs.getString(8));
                jTextField7.setText(rs.getString(9));
                jTextField8.setText(rs.getString(10));
                jTextField6.setText(rs.getString(11));
                jTextField1.setText(rs.getString(12));

                int ph = Integer.parseInt(jTextField2.getText());
                int em = Integer.parseInt(jTextField7.getText());
                int db = Integer.parseInt(jTextField8.getText());
                int os = Integer.parseInt(jTextField6.getText());
                int cn = Integer.parseInt(jTextField1.getText());

                String totalSum = String.valueOf(ph + em + db + os + cn);
                jTextField9.setText(totalSum);
            }
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

